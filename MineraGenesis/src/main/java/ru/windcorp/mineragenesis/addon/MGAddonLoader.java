package ru.windcorp.mineragenesis.addon;

@FunctionalInterface
public interface MGAddonLoader {
	
	public static class Metadata {
		public String id, name, version;

		public Metadata(String id, String name, String version) {
			this.id = id;
			this.name = name;
			this.version = version;
		}

		@Override
		public int hashCode() {
			final int prime = 31;
			int result = 1;
			result = prime * result + ((id == null) ? 0 : id.hashCode());
			return result;
		}

		@Override
		public boolean equals(Object obj) {
			if (this == obj)
				return true;
			if (obj == null)
				return false;
			if (getClass() != obj.getClass())
				return false;
			Metadata other = (Metadata) obj;
			if (id == null) {
				if (other.id != null)
					return false;
			} else if (!id.equals(other.id))
				return false;
			return true;
		}

		@Override
		public String toString() {
			return "Metadata [id=" + id + ", name=" + name + ", version=" + version + "]";
		}
	}
	
	void run() throws Exception;

}
